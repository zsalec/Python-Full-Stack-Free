# 运算符
# 指数
a = 7 ** 2
print('7 ** 2 = ', a)

# 比较运算符
# 等于 ==
a = 3 == 4
print('3 == 4 is ', a)

# 不等于 !=
a = 3 != 4
print('')

# 其他比较运算符
# >, >=, <, <=
b = 3 >= 8
print('3 >= 8 is ', b)

# 赋值运算符 =
a = 9

# 复杂赋值
a = b = 9
a, b = 1, 2

# 赋值的缩写
# +=, -=, *=, /=, //=, **=, %=
cc = 100
cc = cc + 3  # cc 使用前必须赋值（初始化）
print('after cc = cc + 3, cc is', cc)
cc += 3
print('after cc += 3, cc is ', cc)

# 逻辑运算符
# and, or, xor, not
# True is 1, False is 0
