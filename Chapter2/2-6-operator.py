# 由一个或多个数字或变量或运算符组成的代码
1 + 2
a = 1 + 2

# 运算符
# 分类
# - 算术
# - 比较或者关系
# - 赋值
# - 逻辑
# - 位
# - 成员
# - 身份

# 算术运算符
# - 没有自增、自减运算符
a = 9 - 2
print('9 - 2 = ', a)

b = 9 + 2
print('9 + 2 = ', b)

c = 8 * 2
print('8 * 2 = ', c)

# 除法 整除，取模，除
d = 9 / 2   # 除
print('9 / 2 = ', d)
e = 9 // 2  # 整除
print('9 // 2 = ', e)
f = 9 % 2   # 取模
print('9 % 2 = ', f)
# 取模结果的符号要和除数一致
g = 9 % -4  # 取模 -3
print('9 % -4 = ', g)
